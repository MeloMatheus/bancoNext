package br.com.next.beans;

public class Seguro {
	
	private String id;
	private String nomeSeguro;
	private String regraSeguro;

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNomeSeguro() {
		return nomeSeguro;
	}
	public void setNomeSeguro(String nomeSeguro) {
		this.nomeSeguro = nomeSeguro;
	}
	public String getRegraSeguro() {
		return regraSeguro;
	}
	public void setRegraSeguro(String regraSeguro) {
		this.regraSeguro = regraSeguro;
	}
	
	

}
